document.querySelector('.save').addEventListener('click', () => {
    const backdropRect = document.getElementById('backdrop').getBoundingClientRect();
    const canvas = document.createElement('canvas');
    canvas.width = backdropRect.width;
    canvas.height = backdropRect.height;
    const ctx = canvas.getContext('2d');
    ctx.fillStyle = 'white';
    ctx.fillRect(0, 0, ctx.canvas.width, ctx.canvas.height);
 
    const imgLoadEvents = [];
 
    Array.from(document.querySelectorAll('#container img'))
        .filter((img) => {
            const rect = img.getBoundingClientRect();
            const isIntersectingBackdrop = Boolean(
                rect.top >= backdropRect.top &&
                rect.right <= backdropRect.right &&
                rect.bottom <= backdropRect.bottom &&
                rect.left >= backdropRect.left
            );
            return isIntersectingBackdrop;
        })
        .forEach((img) => {
            const rect = img.getBoundingClientRect();
            const newImg = new Image();
            newImg.crossOrigin = 'anonymous';
            newImg.src = img.src;
            imgLoadEvents.push({
                promise: newImg.decode(),
                callback: () => {
                    ctx.drawImage(newImg, rect.left - backdropRect.left, rect.top - backdropRect.top, img.width, img.height);
                }
            });
        });
 
    Promise.all(imgLoadEvents.map((i) => i.promise)).then(() => {
        imgLoadEvents.forEach((i) => i.callback());
        canvas.toBlob((blob) => {
            const url = URL.createObjectURL(blob);
            const a = document.createElement('a');
            a.download = 'stardoll.png';
            a.href = url;
            a.click();
            URL.revokeObjectURL(url);
        });
    });
});
